# mypy: warn_unused_ignores=False
@pytest.mark.asyncio
async def test_post_project_emits_project_created_ws_event(api_db: ApiDb) -> None:
    """A successful create publishes ``project.created`` on ``workspace:<wsId>``."""
    import fakeredis
    import fakeredis.aioredis
    from asgi_lifespan import LifespanManager
    from httpx import ASGITransport, AsyncClient

    user = await api_db.seed_user(email="pw-ws-event@example.com")
    ws = await api_db.seed_workspace(slug="pw-ws-event-ws", name="W")
    await api_db.seed_membership(workspace_id=ws.id, user_id=user.id, role=Role.ADMIN)

    server = fakeredis.FakeServer()
    redis_client = fakeredis.aioredis.FakeRedis(server=server, decode_responses=False)
    received: list[bytes] = []

    app = api_db.app_for(user)
    app.state.ws_redis = redis_client

    pubsub = redis_client.pubsub()
    await pubsub.subscribe(f"workspace:{ws.id}")

    async def _drain() -> None:
        await pubsub.get_message(ignore_subscribe_messages=False, timeout=1.0)
        for _ in range(5):
            msg = await pubsub.get_message(ignore_subscribe_messages=True, timeout=1.0)
            if msg is not None:
                received.append(msg["data"])
                return

    async with LifespanManager(app):
        transport = ASGITransport(app=app)
        async with AsyncClient(transport=transport, base_url="http://test") as c:
            resp = await c.post(
                "/api/v1/projects",
                json={"name": "WS", "slug": "ws-event"},
                headers={"X-Workspace-Id": ws.id},
            )
            assert resp.status_code == 201
            await _drain()

    await pubsub.aclose()  # type: ignore[no-untyped-call]
    await redis_client.aclose()  # type: ignore[no-untyped-call]
    assert received, "WS publish must reach the workspace:<id> channel"
    decoded = received[0].decode()
    assert "project.created" in decoded
