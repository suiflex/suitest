@pytest.mark.asyncio
async def test_delete_project_with_confirmCascade_cascade_tombstones(api_db: ApiDb) -> None:
    """``?confirmCascade=true`` cascades project + suites + cases."""
    user = await api_db.seed_user(email="pw-del-cascade@example.com")
    ws = await api_db.seed_workspace(slug="pw-del-cascade-ws", name="W")
    await api_db.seed_membership(workspace_id=ws.id, user_id=user.id, role=Role.ADMIN)
    project = await _project(api_db, ws.id)
    suite_a = await _suite(api_db, project.id, name="A")
    suite_b = await _suite(api_db, project.id, name="B")
    case_1 = await _case(api_db, suite_a.id, public_id="TC-CD1")
    case_2 = await _case(api_db, suite_b.id, public_id="TC-CD2")

    async with api_db.client(user) as c:
        resp = await c.delete(
            f"/api/v1/projects/{project.id}?confirmCascade=true",
            headers={"X-Workspace-Id": ws.id},
        )
    assert resp.status_code == 204, resp.text

    async with api_db.maker() as session:
        p = await session.get(Project, project.id)
        sa = await session.get(Suite, suite_a.id)
        sb = await session.get(Suite, suite_b.id)
        c1 = await session.get(TestCase, case_1.id)
        c2 = await session.get(TestCase, case_2.id)
    assert p is not None and p.deleted_at is not None
    assert sa is not None and sa.deleted_at is not None
    assert sb is not None and sb.deleted_at is not None
    assert c1 is not None and c1.deleted_at is not None
    assert c2 is not None and c2.deleted_at is not None


