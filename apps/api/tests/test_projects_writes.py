async def _project(api_db: ApiDb, ws_id: str, *, slug: str = "pw-proj", name: str = "P") -> Project:
    project = Project(workspace_id=ws_id, slug=slug, name=name)
    await api_db.add_all([project])
    return project


async def _suite(api_db: ApiDb, project_id: str, *, name: str = "S") -> Suite:
    suite = Suite(project_id=project_id, name=name, order=0)
    await api_db.add_all([suite])
    return suite


async def _case(api_db: ApiDb, suite_id: str, *, public_id: str) -> TestCase:
    case = TestCase(
        suite_id=suite_id,
        public_id=public_id,
        name=public_id,
        source=CaseSource.MANUAL,
    )
    await api_db.add_all([case])
    return case


